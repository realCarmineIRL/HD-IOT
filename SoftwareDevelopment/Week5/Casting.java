public class Casting{
    public static void main(String[] args) {
        //implicit Casting
        double num1=10;
        int num2=4;

        System.out.println(num1/num2);


        //explicit Casting
        double number1=10;
        double number2=4;

        System.out.println((int)(number1/number2));
    }
}
